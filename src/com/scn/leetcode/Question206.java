public class Question206 {
    public ListNode reverseList(ListNode head) {
        if(head == null && head.next == null ){
            return head;
        }
        ListNode pre = null;
        ListNode cur = head;
        ListNode next = head.next;

        while (next !=null){
            cur.next = pre;
            pre = cur;
            cur = next;
            next = next.next;

        }
        cur.next = pre;
        return cur;

    }


    class ListNode {
        int val;
        ListNode next;

        ListNode() {
        }

        ListNode(int val) {
            this.val = val;
        }

        ListNode(int val, ListNode next) {
            this.val = val;
            this.next = next;
        }
    }
}
